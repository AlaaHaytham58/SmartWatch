RTC_SETUP FUNCTION
	PUSH {R0-R12, LR}
	 ; Enable GPIOB clock
    LDR R0, =RCC_APB2ENR     ; RCC_APB2ENR address
    LDR R1, [R0]             ; Read current value
    ORR R1, R1, #0x00000002  ; Enable GPIOB clock (bit 1)
    STR R1, [R0]             ; Write back to RCC_APB2ENR
	POP{R0-R12, PC}
	ENDFUNC
	
